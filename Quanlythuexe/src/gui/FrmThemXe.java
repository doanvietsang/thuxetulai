    private void butUpdatexeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butUpdatexeActionPerformed
        try {
 xedao = new XeDAO();
int giathue = Integer.parseInt(txtGiathuexe.getText());
XeDTO xedto1 = new XeDTO(txtMaxe.getText(), txtTenxe.getText(), txtBienso.getText()
, cbLoaixe.getSelectedItem().toString(), cbTrangthai.getSelectedItem().toString(), giathue);
if(xedao.update(xedto1)){
  int index = xe.tableXe.getSelectedRow();
   listxe.set(index, xedto1);
  xe.reserttalble();
  xe.tableXe.setRowSelectionInterval(index, index);
   xe.tableXe.updateUI();
   xe.tableXe.getColumnModel().getColumn(1).setPreferredWidth(200);
   xe.tableXe.getColumnModel().getColumn(0).setPreferredWidth(20);
   xe.resetP();
   
xe.tabSoluong.updateUI();
JOptionPane.showMessageDialog(this, "ĐÃ CHỈNH SỬA");
this.dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmThemXe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_butUpdatexeActionPerformed
