    private void butTaoxeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTaoxeActionPerformed
        validata();
        if(validata().length() > 0 ){
        JOptionPane.showConfirmDialog(this, validata(), "LỖI", JOptionPane.YES_NO_OPTION);
        }
        else{
        int giathue = Integer.parseInt(txtGiathuexe.getText());
         xedao = new XeDAO();
        XeDTO xedto = new XeDTO(txtMaxe.getText(), txtTenxe.getText(), txtBienso.getText()
        , cbLoaixe.getSelectedItem().toString(), cbTrangthai.getSelectedItem().toString(),giathue);
        if(xedao.create(xedto)){ 
            listxe.add(xedto);
             Vector v = new Vector();
                v.add(xedto.getMaXe());
                v.add(xedto.getTenXe());
                v.add(xedto.getBienSo());
                v.add(xedto.getLoaiXe());
                v.add(xedto.getTrangthai());
                v.add(xedto.getGiathue());
                xe.row.add(v);
                model = new DefaultTableModel(xe.row, xe.col);
                xe.tableXe.setModel(model);
                xe.tableXe.updateUI();
                xe.tableXe.getColumnModel().getColumn(1).setPreferredWidth(200);
        xe.tableXe.getColumnModel().getColumn(0).setPreferredWidth(20);
            xe.resetP();
            xe.tabSoluong.updateUI();
        JOptionPane.showMessageDialog(this, "TẠO THÀNH CÔNG");
        }
        else{
        JOptionPane.showMessageDialog(this, "XE ĐÃ TỒN TẠI \n HẢY KIỂM TRA LẠI BIỂN SỐ VÀ MÃ XE");
        }
        }
    }//GEN-LAST:event_butTaoxeActionPerformed

