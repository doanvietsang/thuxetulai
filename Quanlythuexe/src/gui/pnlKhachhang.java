    private void butUpdateKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butUpdateKHActionPerformed
int index = TableKH.getSelectedRow();
if(index == -1){
    JOptionPane.showMessageDialog(this, "CHỌN KHÁCH HÀNG");
}
else{
 KhachHangDTO dto = listkh.get(index);
FrmthemKH themkh = new FrmthemKH(this);
themkh.txtMakh.setEnabled(false);
themkh.butUpdateKhachH.setEnabled(true);
themkh.butTaokhachH.setEnabled(false);
themkh.txtMakh.setText(dto.getMakh());
themkh.txtTenkh.setText(dto.getTenkh());
themkh.txtSdt.setText(dto.getSdt());
themkh.txtDiachi.setText(dto.getDiachi());
themkh.txtCmnd.setText(dto.getAnhcm());
themkh.txtAnhgp.setText(dto.getAnhgp());
BufferedImage icon = null;
    try {
        icon = ImageIO.read(new File(dto.getAnhcm()));
    } catch (IOException ex) {
        Logger.getLogger(pnlKhachhang.class.getName()).log(Level.SEVERE, null, ex);
    }
Image image = icon.getScaledInstance(themkh.labhienanhCM.getWidth(), themkh.labhienanhCM.getHeight(), Image.SCALE_SMOOTH);
ImageIcon imageicon = new ImageIcon(image);
themkh.labhienanhCM.setIcon(imageicon);


try {
        icon = ImageIO.read(new File(dto.getAnhgp()));
    } catch (IOException ex) {
        Logger.getLogger(pnlKhachhang.class.getName()).log(Level.SEVERE, null, ex);
    }
Image image1 = icon.getScaledInstance(themkh.labhienanhgp.getWidth(), themkh.labhienanhgp.getHeight(), Image.SCALE_SMOOTH);
ImageIcon imageicon1 = new ImageIcon(image1);
themkh.labhienanhgp.setIcon(imageicon1);
themkh.setVisible(true);
}
    }//GEN-LAST:event_butUpdateKHActionPerformed

    private void txtTimKiemsdtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTimKiemsdtActionPerformed
KhachHangDAO dao = new KhachHangDAO();
listkh = new ArrayList<>();
listkh = dao.ReadBySdt(txtTimKiemsdt.getText());
ResertTable();
TableKH.updateUI();
TableKH.getColumnModel().getColumn(3).setPreferredWidth(500);
if(txtTimKiemsdt.getText().equals("")){
listkh = dao.readByAll();
ResertTable();
TableKH.updateUI();
pnlKhachhang.TableKH.getColumnModel().getColumn(3).setPreferredWidth(500);
}
    }//GEN-LAST:event_txtTimKiemsdtActionPerformed

    private void butXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butXoaActionPerformed
int index = TableKH.getSelectedRow();
if(index == -1){
 JOptionPane.showMessageDialog(this, "CHỌN KHÁCH HÀNG");
}
else{
    if(JOptionPane.showConfirmDialog(this, "BẠN MUỐN XÓA KHÁCH HÀNG NÀY", "XÓA", JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
    KhachHangDTO dto = listkh.get(index);
    listkh.remove(index);
    KhachHangDAO dao = new KhachHangDAO();
    if(dao.delete(dto)){
    row.remove(index);
    TableKH.updateUI();
    TableKH.getColumnModel().getColumn(3).setPreferredWidth(500);
    labelCmnd.setIcon(null);
    labelBanglai.setIcon(null);
}
}
}
    }//GEN-LAST:event_butXoaActionPerformed

