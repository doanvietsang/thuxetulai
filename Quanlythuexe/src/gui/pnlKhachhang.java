public void ResertTable(){
     row = new Vector();
       for (KhachHangDTO dto : listkh){
           Vector v = new Vector();
           v.add(dto.getMakh());
          v.add(dto.getTenkh());
          v.add(dto.getSdt());
           v.add(dto.getDiachi());
           row.add(v);
       }
  model = new DefaultTableModel(row, col);
 TableKH.setModel(model);
}
