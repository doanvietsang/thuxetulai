    void resert() {
        txtMakh.setText("");
        txtTenkh.setText("");
        txtSdt.setText("");
        txtDiachi.setText("");
        txtCmnd.setText("");
        txtAnhgp.setText("");
        labhienanhCM.setIcon(null);
        labhienanhCM.validate();
        labhienanhgp.setIcon(null);
        labhienanhgp.validate();

    }

    String validata() {
        String mess = "";
        if (txtMakh.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG MÃ KHÁCH HÀNG\n";
        }
        if (txtTenkh.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG TÊN KHÁCH HÀNG\n ";
        }
        if (txtDiachi.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG ĐỊA CHỈ\n";
        }
        if (txtSdt.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG SỐ ĐIỆN THOẠI\n";
        }
        if (txtCmnd.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG ẢNH CMND\n";
        }
        if (txtAnhgp.getText().equals("")) {
            mess += "KHÔNG ĐƯỢC BỎ TRỐNG ẢNH BẲNG LÁI XE";
        }
        return mess;
    }

