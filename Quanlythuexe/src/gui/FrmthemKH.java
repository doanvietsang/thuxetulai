    private void butTaokhachHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTaokhachHActionPerformed
        validata();
        if (validata().length() > 0) {
            JOptionPane.showMessageDialog(this, validata(), "Lỗi", JOptionPane.OK_OPTION);
        } else {
            KhachHangDAO dao = new KhachHangDAO();
            KhachHangDTO dto = new KhachHangDTO(txtMakh.getText(), txtTenkh.getText(), txtSdt.getText(), txtDiachi.getText(), txtCmnd.getText(), txtAnhgp.getText());
            if (dao.create(dto)) {
                listkh.add(dto);
                Vector v = new Vector();
                v.add(dto.getMakh());
                v.add(dto.getTenkh());
                v.add(dto.getSdt());
                v.add(dto.getDiachi());
                pnlkh.row.add(v);
                model = new DefaultTableModel(pnlkh.row, pnlkh.col);
                pnlkh.TableKH.setModel(model);
                pnlkh.TableKH.updateUI();
                JOptionPane.showMessageDialog(this, "ĐÃ THÊM KHÁCH HÀNG");
                resert();
                pnlKhachhang.TableKH.getColumnModel().getColumn(3).setPreferredWidth(500);
            } else {
                JOptionPane.showMessageDialog(this, "ĐÃ TỒN TẠI KHÁCH HÀNG NÀY \n KIỂM TRA LẠI MÃ KHÁCH HÀNG VÀ SỐ ĐIỆN THOẠI");
            }
        }
    }//GEN-LAST:event_butTaokhachHActionPerformed

    private void butUpdateKhachHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butUpdateKhachHActionPerformed
        int index = pnlkh.TableKH.getSelectedRow();
        KhachHangDAO dao = new KhachHangDAO();
        KhachHangDTO dto = new KhachHangDTO(txtMakh.getText(), txtTenkh.getText(), txtSdt.getText(), txtDiachi.getText(), txtCmnd.getText(), txtAnhgp.getText());
        try {
            if (dao.update(dto)) {
                listkh.set(index, dto);
                pnlkh.ResertTable();
                pnlkh.TableKH.updateUI();
                pnlKhachhang.TableKH.getColumnModel().getColumn(3).setPreferredWidth(500);
                JOptionPane.showMessageDialog(this, "CHỈNH SỬA THÀNH CÔNG");
                BufferedImage icon = null;

        try {
            icon = ImageIO.read(new File(txtCmnd.getText()));
        } catch (IOException ex) {
            Logger.getLogger(FrmthemKH.class.getName()).log(Level.SEVERE, null, ex);
        }

        Image image = icon.getScaledInstance(pnlkh.labelCmnd.getWidth(), pnlkh.labelCmnd.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageicon = new ImageIcon(image);
        pnlkh.labelCmnd.setIcon(imageicon);
        BufferedImage icon1 = null;

        try {
            icon1 = ImageIO.read(new File(txtAnhgp.getText()));
        } catch (IOException ex) {
            Logger.getLogger(FrmthemKH.class.getName()).log(Level.SEVERE, null, ex);
        }

        Image image1 = icon1.getScaledInstance(pnlkh.labelBanglai.getWidth(), pnlkh.labelBanglai.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageicon1 = new ImageIcon(image1);
        pnlkh.labelBanglai.setIcon(imageicon1);
        pnlkh.TableKH.setRowSelectionInterval(index, index);
         this.dispose();
         
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(FrmthemKH.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_butUpdateKhachHActionPerformed

